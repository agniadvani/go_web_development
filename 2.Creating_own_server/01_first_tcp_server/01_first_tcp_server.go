package main

import (
	"fmt"
	"io"
	"log"
	"net"
)

func main() {
	li, err := net.Listen("tcp", ":8080")
	if err != nil {
		log.Println(err)
	}
	defer li.Close()

	for {
		conn, err := li.Accept()
		if err != nil {
			log.Println(err)
		}

		io.WriteString(conn, "\nHello from tcp server\n")
		fmt.Fprintln(conn, "writing on :8080")
		fmt.Fprintf(conn, "%v", "well i hope")

		conn.Close()

	}
}
